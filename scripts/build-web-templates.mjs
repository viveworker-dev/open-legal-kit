#!/usr/bin/env node

import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const rootDir = path.resolve(__dirname, "..");
const templateDir = path.join(rootDir, "templates");
const outFile = path.join(rootDir, "web", "templates.js");

const locales = ["en", "ja", "zh-Hans"];
const documents = [
  "privacy-policy",
  "terms-of-service",
  "account-data-deletion",
  "payment-refund-terms",
  "ai-content-disclaimer",
  "launch-checklist"
];

const templates = {};

for (const locale of locales) {
  templates[locale] = {};
  for (const document of documents) {
    const file =
      locale === "en"
        ? path.join(templateDir, `${document}.md`)
        : path.join(templateDir, locale, `${document}.md`);
    templates[locale][document] = await fs.readFile(file, "utf8");
  }
}

const source = `// Generated by scripts/build-web-templates.mjs. Do not edit by hand.
export const TEMPLATES = ${JSON.stringify(templates, null, 2)};
`;

await fs.mkdir(path.dirname(outFile), { recursive: true });
await fs.writeFile(outFile, source, "utf8");
console.log(`Wrote ${path.relative(rootDir, outFile)}`);
